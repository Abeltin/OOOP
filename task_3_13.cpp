#include <iostream>

class Calculator {
public:
    int add(int a, int b); // Объявление метода
};

// Определение метода вне класса
int Calculator::add(int a, int b) {
    return a + b;
}

int main() {
    Calculator calc;
    std::cout << "Сумма: " << calc.add(3, 4) << std::endl;
    return 0;
}

